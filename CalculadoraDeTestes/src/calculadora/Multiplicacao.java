public class Multiplicacao {
	/** 
	 * @param a, primeiro valor utilizado para multiplicação
	 * @param b, segundo valor utilizado para multplicação
	 * @return , resultado de retorno
	 *  **/
	public int multiplicar(int a, int b) {
        return a * b;
    }
}
