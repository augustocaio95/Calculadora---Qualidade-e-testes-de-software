public class Soma {
	/** 
	 * @param a, primeiro valor utilizado para soma
	 * @param b, segundo valor utilizado para soma
	 * @return , resultado de retorno
	 *  **/
	public int somar(int a, int b) {
        return a + b;
    }
}
