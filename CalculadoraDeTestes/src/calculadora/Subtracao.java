public class Subtracao {
	/** 
	 * @param a, primeiro valor utilizado para subtração
	 * @param b, segundo valor utilizado para subtração
	 * @return , resultado de retorno
	 *  **/
	
	public int subtrair(int a, int b) {
        return a - b;
    }
}
